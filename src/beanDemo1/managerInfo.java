package beanDemo1;

public class managerInfo {
	
	private String name=null;
	private String psw=null;
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name 要设置的 name
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return psw
	 */
	public String getPsw() {
		return psw;
	}
	/**
	 * @param psw 要设置的 psw
	 */
	public void setPsw(String psw) {
		this.psw = psw;
	}
	
	

}
